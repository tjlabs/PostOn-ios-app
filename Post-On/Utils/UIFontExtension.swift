import UIKit

extension UIFont {
    class func lemonRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "Lemon-Regular", size: size)!
    }
    
    class func lemonadaRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "Lemonada-Regular", size: size)!
    }
    
    class func lemonadaBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Lemonada-Bold", size: size)!
    }
    
    class func lemonadaSemiBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Lemonada-SemiBold", size: size)!
    }
    
    class func lemonadaMedium(size: CGFloat) -> UIFont {
        return UIFont(name: "Lemonada-Medium", size: size)!
    }
    
    class func lemonadaLight(size: CGFloat) -> UIFont {
        return UIFont(name: "Lemonada-Light", size: size)!
    }
}
